# ============================================================
semua_negara_master = df_master['translated_location'].dropna().unique()
fallback_rows = []

for negara in semua_negara_master:
    negara_clean = str(negara).strip()
    existing = df_final_map[(df_final_map['translated_location'] == negara_clean) & (df_final_map['cluster_risiko'].notnull())]

    if existing.empty:
        # Cari jejak string kualitatif dari rekam jejak history Spark manapun (Lintas Tahun/Lintas Metrik)
        histori_spark = df_pred[(df_pred['translated_location'] == negara_clean) & (df_pred['cluster_risiko'].notnull())]
        data_m2023 = df_master[(df_master['translated_location'] == negara_clean) & (df_master['year'] == 2023)]

        if not data_m2023.empty:
            actual_row = data_m2023.iloc[0]
            if not histori_spark.empty:
                # Ambil keputusan historis string asli buatan Spark Session Anda
                cluster_string = histori_spark.sort_values('year', ascending=False).iloc[0]['cluster_risiko']
                profil_string = histori_spark.sort_values('year', ascending=False).iloc[0]['profil_negara']
            else:
                # Interpolasi kaku berdasarkan sebaran batas kuantil Spark Kelompok 1
                val_laju = actual_row['val']
                cluster_string = 'Tinggi' if val_laju > 694.63 else ('Sedang' if val_laju > 281.12 else 'Rendah')
                profil_string = f"Risiko {cluster_string}"

            fallback_rows.append({
                'translated_location': negara_clean, 'location_name': negara_clean, 'year': 2023,
                'measure_name': 'DALYs (Disability-Adjusted Life Years)', 'metric_name': 'Laju',
                'val': actual_row['val'], 'prediction': actual_row['val'],
                'cluster_risiko': cluster_string, 'profil_negara': profil_string,
                'gdp_per_capita': actual_row.get('gdp_per_capita', np.nan),
                'pop_density': actual_row.get('pop_density', np.nan)
            })

if fallback_rows:
    df_final_map = pd.concat([df_final_map, pd.DataFrame(fallback_rows)], ignore_index=True)

